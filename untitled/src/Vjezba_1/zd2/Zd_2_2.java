package Vjezba_1.zd2;


public class Zd_2_2 {
    public static void main(String[] args) {

        // Primjena operacija +, -, *, % i / za int
        int a = 10;
        int b = 3;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

        // Primjena korjenovanja i nala≈æenja apsolutne vrijednosti za float
        float c = -9.81f;
        System.out.println("Square root of |c| = " + Math.sqrt(Math.abs(c)));
    }
}
